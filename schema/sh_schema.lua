local STAFFROLES = {
	superadmin = true,
	root = true,
	superadministrator = true,
	senioradministrator = true,
	administrator = true,
	moderator = true,
	gamemaster = true,
}

nut.chat.register("sc", {
	onCanSay =  function(speaker, text)
		if(!STAFFROLES[speaker:GetUserGroup()]) then
			speaker:notify("You do not have the permissions to do this.")
			return false 
		end
		if(STAFFROLES[speaker:GetUserGroup()]) then
			return true
		end
	end,
	
	onChatAdd = function(speaker, text)
		chat.AddText(Color(78, 205, 64), "STAFF/GAMEMASTER CHAT", Color(255, 255, 255), " | ", team.GetColor(speaker:Team()), speaker:steamName() .. ": ",  color_white, text)
	end,
	onCanHear = function(speaker, listener)
	if(STAFFROLES[listener:GetUserGroup()]) then
		return true else return false
	end
	end,
	prefix = {"/sc"},
	noSpaceAfter = true,
	filter = "OOC"
})

